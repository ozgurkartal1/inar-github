package week_03.live_class;

public class Workspace06 {

    public static void main(String[] args) {
        boolean booleanValue = true;
        System.out.println("Boolean value -> " + booleanValue);
        System.out.println("Boolean !value -> " + !booleanValue);

        boolean andOperator = true && false;
        System.out.println("&& Operator -> " + andOperator);

        boolean orOperator = true || false;
        System.out.println("|| Operator -> " + orOperator);

        boolean exclusiveOperator = true ^ false;
        System.out.println("^ Operator -> " + exclusiveOperator);
    }
}
